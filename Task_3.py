import math

x=int(input("შეიყვანე სამკუთხედის ერთი გვერდის სიგრძე"))
y=int(input("შეიყვანე სამკუთხედის მეორე გვერდის სიგრძე"))
z=int(input("შეიყვანე სამკუთხედის მესამე გვერდის სიგრძე"))
p=x+y+z
s=math.sqrt(p*(p-x)*(p-y)*(p-z))
# ჰერონის ფორმულით  გავყევი რადგან არ ვიცოდი სხვა დანარჩენი მონაცემები,
# აქ უცხო იყო ფესვი რომელიც არ ვიცოდი და იმედია სწორად წამოიღო

print("სამკუთხედის პერიმეტრი:",p)
print("სამკუთხედის ფართობი:",s)